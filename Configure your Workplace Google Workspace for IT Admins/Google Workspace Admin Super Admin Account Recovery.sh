# Google Workspace Admin: Super Admin Account Recovery
# 1 hour
# 1 Credit
# GSP948
# Google Cloud self-paced labs logo

# Overview
# As a super admin, you configure account recovery for user accounts, including the feature that allows users to reset their own password. But what if you forget your password? In this lab you explore the options and best practices for super admin account recovery. You add recovery information to your super admin account and reset your super admin password.

# Objectives
# In this lab you do the following in the Google Workspace Admin Console:

# Enable super admin account recovery

# Add recovery options to your super admin account

# Reset your password using a recovery email

# Prerequisites
# For this lab you need an email account you can access outside of this lab. You will send and retrieve a recovery code to verify your account.

# Familiarity with basic Google Workspace terminology is recommended to your learning experience.

# Setup and requirements
# Before you click the Start Lab button
