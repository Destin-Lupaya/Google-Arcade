# Start your lab
# When you are ready, click Start Lab in the upper left.

# Sign in to the Google Workspace Admin Console
# To access the Google Workspace Admin Console, you must find your credentials and then sign in.

# Find your lab's User Email and Password
# To access the resources and console for this lab, locate the User Email and Password in the Lab Details panel. This panel is on the left or at the top, depending on the width of the browser window. Use these credentials to log in to the Google Workspace Admin Console.

# If your lab requires other resource identifiers or connection-related information, they will appear on this panel as well.

# Sign in to the Admin Console
# Click Open Google Workspace Admin Console.
# Tip: Open the tabs in separate windows, side-by-side.

# Note: If you see the Verify your account dialog:
# Click Next.
# Click the prefilled user.
# Click Use another account.
# Enter the User Email and Password.

# Accept all terms and conditions as prompted.

# The Admin Console opens.

# Click VERIFY DOMAIN in either the yellow box at the top or the red box in the Domains card.

# Click Next.

# In the Welcome, let's set up Google Workspace dialog, in section 1, click VERIFY. Google verifies your training domain.

# Ignore step 2, Create new users and step 3, Activate Gmail sections.

# Click Google Admin in the top left to open the Google Workspace Admin Console home page.
# Task 1. View super admin recovery settings
# View the recovery options available in Google Workspace and the default settings of user passwords.

# In the Google Admin console, click the Security card. You may have to scroll down and click Show more to see the Security card.
# Scroll down and click to expand the Account Recovery section. This is where you configure account recovery policies for the super admin and users.
# By default, account recovery is ON for a super admin and Off for users.

# Task 2. Add recovery information to your administrator account
# In real life, you supply recovery information when you sign up for your Google Workspace account. In this lab however, the administrator account was created for you in the training domain, so in this section, you add your recovery information.

# Open your Workspace account.
# Click Security either in the left panel or on the top depending on the browser width.
# Note: The Security option is on either the left panel or the top menu bar, depending on the width of your browser window.
# Scroll down to the Ways we can verify it's you section.
# Click Recovery email.
# When prompted, enter your password from the Lab Details panel, and then click Next.
# Enter your email address, and then click NEXT.
# At this point, Google Sends a verification code to your recovery email address.

# Go to the recovery email account and open the message from Google that provides your verification code.
# Copy the verification code into the Verification code field and click Verify.
# Your recovery email address is verified.

# Close the Recovery email browser tab.
# Click Check my progress to verify the objective.
# Add recovery information to your administrator account

# Task 3. Recover your administrator account
# In this next section, you sign out of your account, then sign in again. However, imagine that you have forgotten your password so you need to recover your account.

# In your Workspace account, click the user avatar at the top right and click Sign out to sign out of the account.
# Sign in using the User Email provided in the Lab Details pane, but when prompted for your password, click the Forgot password link.
# Google emails you a verification code.
# Go to your recovery email account and find the message from Google with the verification code
# Enter the code in the Account recovery screen, then click Next.
# You are now asked to reset your password.

# Enter a new password, confirm the password, for example luv2learn, then click Save password.
# Click Continue to return to the Admin console.
# You have successfully recovered your super admin account.

# Click Check my progress to verify the objective.
# Recover your administrator account

